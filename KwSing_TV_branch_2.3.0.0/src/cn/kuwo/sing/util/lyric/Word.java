package cn.kuwo.sing.util.lyric;

public class Word {
	private String mContent;  //字内容
	private int mIndex;      //索引
	private long mTimestamp;  //字开始时间
	private int mTimespan;    //字持续时间
	private int mEnvelope;	// 包络值

	public String getContent() {
		return mContent;
	}

	public void setContent(String value) {
		mContent = value;
	}

	public int getIndex() {
		return mIndex;
	}

	public void setIndex(int value) {
		mIndex = value;
	}

	public long getTimestamp() {
		return mTimestamp;
	}

	public void setTimestamp(Long value) {
		mTimestamp = value;
	}

	public int getTimespan() {
		return mTimespan;
	}

	public void setTimespan(int timespan) {
		mTimespan = timespan;
	}

	public int getEnvelope() {
		return mEnvelope;
	}

	public void setEnvelope(int mEnvelope) {
		this.mEnvelope = mEnvelope;
	}
}
